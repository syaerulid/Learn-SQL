# How to Use IfNull Function in SQL

USE FAZA;

CREATE TABLE EMPONE (ID INT, SALARY INT);

SELECT *
FROM EMPONE;

INSERT INTO EMPONE VALUES(1, 100000);
INSERT INTO EMPONE VALUES(2, 700000);
INSERT INTO EMPONE(ID) VALUES(3);

SELECT (SALARY+100000) FROM EMPONE; # ALL THE VALUE WILL GET + 100000

SELECT (IFNULL(SALARY,0)+100000) FROM EMPONE; # SO IF SALARY VALUE IS NULL, IT WILL GET + BY 100000